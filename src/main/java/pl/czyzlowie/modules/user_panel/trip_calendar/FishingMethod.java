package pl.czyzlowie.modules.user_panel.trip_calendar;

/**
 * Enum representing different fishing methods that can be used during a fishing trip.
 * This is primarily used to categorize trips based on the fishing technique.
 *
 * The available methods are:
 * - SPINNING: A method involving the use of a spinning rod and reel.
 * - KARPIOWANIE: A technique focusing on carp fishing.
 * - FEEDER: A method using a feeder rod, usually for bottom fishing.
 * - SPLAWIK: A float fishing technique.
 * - MORSKIE: Sea or marine fishing.
 */
public enum FishingMethod {
    SPINNING, KARPIOWANIE, FEEDER, SPLAWIK, MORSKIE
}

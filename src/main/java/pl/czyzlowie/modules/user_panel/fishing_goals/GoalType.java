package pl.czyzlowie.modules.user_panel.fishing_goals;

/**
 * Enum representing the types of goals that can be set for fishing activities.
 *
 * The goal types define the specific metric or measure that a user aims to achieve
 * when creating a fishing-related goal.
 *
 * Possible values:
 * - LENGTH: Goals associated with the total length of fish.
 * - WEIGHT: Goals based on the cumulative weight of fish.
 * - QUANTITY: Goals related to the number of fish caught.
 * - TRIPS: Goals focused on the number of fishing trips.
 */
public enum GoalType {
    LENGTH, WEIGHT, QUANTITY, TRIPS
}


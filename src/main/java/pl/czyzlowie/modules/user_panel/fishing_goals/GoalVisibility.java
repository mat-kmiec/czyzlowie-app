package pl.czyzlowie.modules.user_panel.fishing_goals;

/**
 * Enum representing the visibility levels of a fishing goal.
 *
 * The visibility of a fishing goal determines its accessibility to other users.
 * - PRIVATE: The goal is only visible to the user who created it.
 * - GLOBAL: The goal is visible to all users.
 */
public enum GoalVisibility {
    PRIVATE, GLOBAL
}

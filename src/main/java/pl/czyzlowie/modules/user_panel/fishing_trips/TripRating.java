package pl.czyzlowie.modules.user_panel.fishing_trips;

/**
 * Represents the rating assigned to a fishing trip.
 *
 * Enum Constants:
 * - EPIC: Represents an outstanding or unforgettable fishing trip.
 * - GOOD: Represents a positive and enjoyable fishing experience.
 * - AVERAGE: Represents a fishing trip that met basic expectations but was not notable.
 * - BLANK: Represents a fishing trip with no specific feedback or rating.
 *
 * This enumeration is used to categorize or evaluate fishing trips based on the quality of the experience,
 * and it is associated with the FishingTrip entity for descriptive purposes.
 */
public enum TripRating {
    EPIC, GOOD, AVERAGE, BLANK


}

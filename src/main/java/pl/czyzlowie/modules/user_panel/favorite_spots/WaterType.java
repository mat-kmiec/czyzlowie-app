package pl.czyzlowie.modules.user_panel.favorite_spots;

/**
 * Enum representing different types of water bodies.
 *
 * This enumeration is used to specify the type of waterbody associated with a
 * fishing location or favorite spot. The water types include:
 * - RIVER: Represents a river waterbody.
 * - LAKE: Represents a lake waterbody.
 * - POND: Represents a pond waterbody.
 * - COMMERCIAL: Represents a commercial waterbody, such as a fishing facility.
 * - SEA: Represents a sea or ocean waterbody.
 */
public enum WaterType {
    RIVER, LAKE, POND, COMMERCIAL, SEA
}